!c ----------------------------------------------------------------------
!c real*8 function gasp_m
!c ----------------------
!c
!c compute gas pressure from molar density
!c
!c input units for molar density are in [moles/l air]
!c ouptut pressure is in [Pa] = [N m^-2]
!c
!c written by: Sergi Molins, May 2, 2006 
!c
!c last modified:
!c
!c modified by: Danyang Su - March 26, 2014
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   real*8:
!c           -------
!c           moldens            = molar density                       + - 
!c                                in control volume i
!c           gasp_m             = gas pressure in control vol i       * +
!c
!c           integer*4:
!c           ----------
!c           ivol               = control volume index                + - 
!c
!c common:   
!c gen.f:    real*8:
!c           -------
!c           tkel(nn)           = nodal temperatures in kelvin        + - 
!c           
!c chem.f:   real*8:
!c           -------
!c           tempk              = temperature in kelvin               + -
!c           ng                 = number of gas species               + -
!c           rgasjoule          = ideal gas constant                  + -
!c
!c local:    real*8:
!c           -------
!c           temp               = temperature 
!c           r10_3              = constant
!c           
!c           integer*4:
!c           ----------
!c           i                  = index
!c
!c external: -  
!c ----------------------------------------------------------------------
 
      real*8 function gasp_m(moldens,ivol)

      use gen, only : tkel
      use chem, only: temp_field, tempk, rgasjoule
 
      implicit none

!c  passed
      real*8, intent(in) :: moldens
      integer, intent(in) :: ivol
    
!c  internal
      real*8 temp, r10_3

!c  constants
      parameter (r10_3 = 1.0d3)

!c  assign temperature

      if (temp_field) then

        temp = tkel(ivol)

      else

        temp = tempk

      end if

!c  calculate pressure (r10_3 converts l air to m^3)
      gasp_m = r10_3 * rgasjoule * temp * moldens
 
      return
      end
