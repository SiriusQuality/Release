!c ----------------------------------------------------------------------
!c real*8 function gasm
!c ---------------------
!c
!c compute molar density
!c
!c input concentrations for concentrations are in [moles/l air]
!c ouptut molar density is in [moles/l air]
!c
!c written by: Sergi Molins, May 2, 2006 
!c
!c last modified:
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   real*8:
!c           -------
!c           gconc(ng)          = gases concentrations                + - 
!c                                in control volume i
!c           gasm               = gas molar density in control vol i  * +
!c
!c common:   
!c chem.f:   real*8:
!c           -------
!c           ng                 = number of gas species               + -
!c
!c local:    real*8:
!c           -------
!c           r0                 = constant
!c           
!c           integer*4:
!c           ----------
!c           i                  = index
!c
!c external: -  
!c ----------------------------------------------------------------------
 
      real*8 function gasm(ng, gconc)


      implicit none
      
      integer, intent(in) :: ng

      real*8, intent(in)  :: gconc(ng)       
      
      !local variable
      
      integer :: i
      
      real*8 :: dummy, r0 

      parameter (r0 = 0.0d0)
      
!c  initialize
      dummy = r0

!c  calculate molar density
      do i = 1,ng
        dummy = dummy + gconc(i)
      enddo
      gasm = dummy
     
      return
      end
