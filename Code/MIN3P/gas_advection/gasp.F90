!c ----------------------------------------------------------------------
!c real*8 function gasp
!c ---------------------
!c
!c compute gas pressure
!c
!c input concentrations for concentrations are in [moles/l air]
!c ouptut pressure is in [Pa] = [N m^-2]
!c
!c written by: Sergi Molins, May 2, 2006 
!c
!c last modified:
!c
!c modified by: Danyang Su - March 26, 2014
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   real*8:
!c           -------
!c           gconc(ng)          = gases concentrations                + - 
!c                                in control volume i
!c           gasp               = gas pressure in control vol i       * +
!c
!c           integer*4:
!c           ----------
!c           ivol               = control volume index                + - 
!c
!c common:   
!c gen.f:    real*8:
!c           -------
!c           tkel(nn)           = nodal temperatures in kelvin        + - 
!c           
!c chem.f:   real*8:
!c           -------
!c           ng                 = number of gas species               + -
!c           rgasjoule          = ideal gas constant                  + -
!c                                [J/(deg K mol)]               
!c
!c local:    real*8:
!c           -------
!c           temp               = temperature 
!c           r0                 = constant
!c           r10_3              = constant
!c           
!c           integer*4:
!c           ----------
!c           i                  = index
!c
!c external: -  
!c ----------------------------------------------------------------------
 
      real*8 function gasp(ng, gconc, ivol)

      use gen, only : tkel
      use chem, only: temp_field, tempk, rgasjoule
 
      implicit none
      
!c  passed
      integer, intent(in) :: ng, ivol
      real*8, intent(in)  :: gconc(ng)

    
!c  internal
      real*8 temp, dummy 
      real*8 :: r0, r10_3
      integer :: i

!c  constants
      parameter (r0 = 0.0d0, r10_3 = 1.0d3)

!c  assign temperature

      if (temp_field) then

        temp = tkel(ivol)

      else

        temp = tempk

      end if

!c  initialize
      dummy = r0

!c  calculate molar density
      do i = 1,ng
        dummy = dummy + gconc(i)
      enddo

!c  calculate pressure (r10_3 converts l air to m^3)
      gasp = r10_3 * rgasjoule * temp * dummy
 
      return
      end
