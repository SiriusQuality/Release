!c	  tstar = tempk / epsil(ig)
!c        omega = rAA / tstar**rBB + rCC / exp(rDD*tstar) +
!c     &          rEE / exp(rFF*tstar)
!c        visc_comp_g(ig) = rcst * 
!c     &                  ( gfwg(ig) * tempk )**rhalf / 
!c     &                  ( sigma(ig)**r2 * omega )
!!       2012-11-06 corrected    
