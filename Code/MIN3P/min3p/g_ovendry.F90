!c ----------------------------------------------------------------------
!c real*8 function gs term for oven dry 
!c ------------------------
!c ----------------------------------------------------------------------
real*8 function g_ovendry(w,m) 
      
      
implicit none 
      
real*8, intent(in)    :: w
      
real*8, intent(in)    :: m

real*8                :: p, &
                         gs_ovendry, &
                         h_ovendry 

integer               :: i 
      
real*8, parameter      :: w0=0.9d0, &
                          r1=1.0d0, &
                          r0=0.0d0


if (w>w0) then
  g_ovendry = gs_ovendry(w0,m) + h_ovendry(r1-w0,m) - h_ovendry(r1-w,m)
else
  g_ovendry = gs_ovendry(w,m)
end if
 
return
end function 