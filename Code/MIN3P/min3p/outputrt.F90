!c  close files
!c  --------------------------------------------------------------------- 
      if(b_output_binary) then          
        call binary_file_close(igst,b_output_mpiio_single)
        if (multi_diff) then
          call binary_file_close(icbt,b_output_mpiio_single)
        end if
        if (flux_out) then
          call binary_file_close(igmf,b_output_mpiio_single)
        end if
        
        if (extended_output) then
          call binary_file_close(igsc,b_output_mpiio_single)
          if (ph_output .or. pe_output) then
            call binary_file_close(igsm,b_output_mpiio_single)
          end if
          if (naq.gt.0.and.(.not.initial_condition)) then
            call binary_file_close(igsi,b_output_mpiio_single)
          end if
          if (ng.gt.0) then
            call binary_file_close(igsg,b_output_mpiio_single)
            if (gas_removal) then
              call binary_file_close(igsgr,b_output_mpiio_single)
            end if
          end if
          if (nsb_ion.gt.0.or.nsb_surf.gt.0.or.noncompetitive_sorption) then
            call binary_file_close(igsb,b_output_mpiio_single)
          end if
          if (nm.gt.0) then
            call binary_file_close(igss,b_output_mpiio_single)
            call binary_file_close(igsd,b_output_mpiio_single)
            call binary_file_close(igsv,b_output_mpiio_single)
          end if
          if (nmx.gt.0) then
            call binary_file_close(igsx,b_output_mpiio_single)
          end if
          if (iso_output) then
            call binary_file_close(igsis,b_output_mpiio_single)  
          end if
          if (chemical_water) then
            call binary_file_close(igsw,b_output_mpiio_single)    
          end if
        end if
      else
        close(igst)
        !Keep the file unit, this will be used later
        !call lun_free(igst)
        if (multi_diff) then
          close(icbt)
          !Keep the file unit, this will be used later
          !call lun_free(icbt)
        end if
        if (flux_out) then
          close(igmf)
          !Keep the file unit, this will be used later
          !call lun_free(igmf)
        end if
        if (extended_output) then
          close(igsc)
          !Keep the file unit, this will be used later
          !call lun_free(igsc)
          if (ph_output .or. pe_output) then
            close(igsm)
            !Keep the file unit, this will be used later
            !call lun_free(igsm)
          end if
          if (naq.gt.0.and.(.not.initial_condition)) then
            close(igsi)
            !Keep the file unit, this will be used later
            !call lun_free(igsi)
          end if
          if (ng.gt.0) then
            !Keep the file unit, this will be used later
            close(igsg)
            !call lun_free(igsg)
            if (gas_removal) then
              close(igsgr)
              !Keep the file unit, this will be used later
              !call lun_free(igsgr)
            end if
          end if
          if (nsb_ion.gt.0.or.nsb_surf.gt.0.or.noncompetitive_sorption) then
            close(igsb)
            !Keep the file unit, this will be used later
            !call lun_free(igsb)
          end if
          if (nm.gt.0) then
            close(igss)
            close(igsd)
            close(igsv)
            !Keep the file unit, this will be used later
            !call lun_free(igss)
            !call lun_free(igsd)
            !call lun_free(igsv)
          end if
          if (nmx.gt.0) then
            close(igsx)
            !Keep the file unit, this will be used later
            !call lun_free(igsx)
          end if          
          if (iso_output) then
            close(igsis)
            !Keep the file unit, this will be used later
            !call lun_free(igsis)
          end if
          
          if (chemical_water) then
            close(igsw)
            !Keep the file unit, this will be used later
            !call lun_free(igsw)
          end if
        
        end if      
      end if
 
      return
