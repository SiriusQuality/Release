            deallocate(realbuffer11)
          end if
          
          if (iso_output) then
            if (b_output_multizone .or. .not.b_output_mpiio_single) then
              call binary_write_data(igsis,size(realbuffer12,1),       &
                           realbuffer12, offset_igsis,                 &
                           b_output_mpiio_single)
