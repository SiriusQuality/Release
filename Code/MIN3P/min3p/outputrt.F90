              call binary_write_data(igsw,size(realbuffer13,1),        &
                           realbuffer13, offset_igsw,                  &
                           mpiarray_filetype_igsw)  
            end if
            deallocate(realbuffer13)
          end if
              
        end if
 
      end if        !(extended_output)
      
      if (b_output_binary) then
        deallocate(tec_variables)
      end if

