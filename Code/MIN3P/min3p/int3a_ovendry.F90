!c ----------------------------------------------------------------------
!c subroutine velovapour
!c compute velocities in the vapour phase 
!c ----------------------------------------------------------------------
real*8 function int3a_ovendry(w,w0,m) 

implicit none 

real*8, intent(in)    :: w

real*8, intent(in)    :: w0

real*8, intent(in)    :: m

real*8                :: g_ovendry, &
                         term, &
                         term1, &
                         term2, &
                         m1  
       

real*8, parameter    :: r1 = 1.0d0

m1 = r1/m
term = (r1-w0)/w0
term1 = ((r1-w0)**m)*(dlog(term) - m1)
term = (r1-w)/w
term2 = ((r1-w)**m)*(dlog(term) - m1)


int3a_ovendry = term1 - term2 + g_ovendry(r1-w,m) - g_ovendry(r1-w0,m)


return
end function 