 real*8 function storevap(delt,tempnew,tempold,satvnew,satvold,densvnew,  &
                          densvold,pornew,porold,cv)            
      
      
 implicit none 
      
 real*8, intent(in)    ::  delt
      
 real*8, intent(in)    ::  tempnew
      
 real*8, intent(in)    ::  tempold
      
 real*8, intent(in)    ::  satvnew
      
 real*8, intent(in)    ::  satvold
      
 real*8, intent(in)    ::  densvnew
      
 real*8, intent(in)    ::  densvold
      
 real*8, intent(in)    ::  pornew
      
 real*8, intent(in)    ::  porold
      
 real*8, intent(in)    ::  cv
      
 real*8, parameter      :: r1=1.0d0

                                  
 storevap = cv*(pornew * satvnew * densvnew * tempnew - & 
                porold * satvold * densvold * tempold)/delt
return
end
