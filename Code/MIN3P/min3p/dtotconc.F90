!c ----------------------------------------------------------------------
!c subroutine dtotconc
!c -------------------
!c
!c compute derivatives of total aqueous component concentrations based 
!c on concentrations of free species and secondary aqueous species
!c
!c written by:      Uli Mayer - February 26, 96
!c
!c last modified:   Uli Mayer - December 16, 96
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   real*8:
!c           -------
!c           c(nc)              = concentrations of free species      + -
!c                                - new time level [moles/l water]
!c           cx(nx)             = concentrations of secondary         + -
!c                                aqueous species
!c                                - new time level [moles/l water]
!c           drtinc             = increment for numerical             + -
!c                                differentiation
!c
!c           integer*4:
!c           ----------
!c           jbl                = pointer to column of block matrix   + -
!c
!c chem.f:   real*8:
!c           -------
!c           cxinc(nx,nthreads) = secondary aqueous species           + -
!c                                concentrations dependent on
!c                                incremented free species
!c                                concentrations
!c                                [moles/l water]
!c           dtotc(n,nthreads)  = derivatives of total aqueous        * +
!c                                component concentrations
!c                                [moles/l water]
!c           xnux(nx*nc)        = stoichiometric coefficient matrix   + -
!c                                for formation of secondary aqueous
!c                                species from free species
!c
!c           integer*4:
!c           ----------
!c           iax(nx+1)          = row pointer array to                + -
!c                                stoichiometric coefficients of
!c                                free species in
!c                                secondary aqueous species
!c           jax(nx*nc)         = column pointer array to             + -
!c                                stoichiometric coefficients of
!c                                free species in secondary aqueous
!c                                species
!c           nc                 = number of components                + -
!c           nx                 = number of secondary aqueous         + -
!c                                species
!c
!c
!c local:    integer*4:
!c           ----------
!c           i                   = counter (stoichiometric
!c                                 coefficients)
!c           ibl                 = counter (components)
!c           ibl2                = counter (components)
!c           istart              = pointer (stoichiometric 
!c                                 coefficients)
!c           istop               = pointer (stoichiometric
!c                                 coefficients)
!c           ix                  = counter (secondary species) 
!c
!c external: -
!c ----------------------------------------------------------------------
  
      subroutine dtotconc(c,cx,drtinc,jbl,izn_opt)
 
      use parm
      use chem
      use gen, only : f_epor_ps, f_epor_ss

#ifdef OPENMP
      use omp_lib 
#endif
 
      implicit none
      
      real*8 :: c, cx, drtinc
      
      integer :: jbl
      
      integer, optional :: izn_opt

      dimension c(*),cx(*)
      
      integer :: tid, i, ibl, ix, istart, istop
      
#ifdef OPENMP
      tid = omp_get_thread_num() + 1
#else
      tid = 1
#endif
 
      if(present(izn_opt)) then
      
      !c  contributions from free species 
 
      do ibl=1,nc-1
        dtotcf(ibl,tid) = (cinc(ibl,tid) - c(ibl))*f_epor_ps(jbl,izn_opt)
      end do
 
!c  sum up contributions from aqueous complexes
 
      do ix=1,nx
        istart = iax(ix)
        istop = iax(ix+1)-1
        do i = istart,istop
          ibl = jax(i)

!c  skip h2o, since not a primary unknown
 
          if (namec(ibl).ne.'h2o') then
            dtotcf(ibl,tid) = dtotcf(ibl,tid) + xnux(i)*                &
                             (cxinc(ix,tid)-cx(ix))*f_epor_ss(ix,izn_opt)
          end if
        end do
      end do
 
!c  form derivative -> divide by increment for numerical
!c  differentiation
 
      do ibl=1,nc-1
        dtotcf(ibl,tid) = dtotcf(ibl,tid)/drtinc
      end do

     else          !optional parameter    
    
!c  contributions from free species 
 
      do ibl=1,nc-1
        dtotc(ibl,tid) = cinc(ibl,tid) - c(ibl)
      end do
 
!c  sum up contributions from aqueous complexes
 
      do ix=1,nx
        istart = iax(ix)
        istop = iax(ix+1)-1
        do i = istart,istop
          ibl = jax(i)

!c  skip h2o, since not a primary unknown
 
          if (namec(ibl).ne.'h2o') then
            dtotc(ibl,tid) = dtotc(ibl,tid) + xnux(i)*                &
                             (cxinc(ix,tid)-cx(ix))
          end if
        end do
      end do
 
!c  form derivative -> divide by increment for numerical
!c  differentiation
 
      do ibl=1,nc-1
        dtotc(ibl,tid) = dtotc(ibl,tid)/drtinc
      end do
      
      end if            !optional parameter
 
      return
      end
