!c ----------------------------------------------------------------------
!c real*8 function zoutput
!c -----------------------
!c
!c assign z-coordinate in terms of elevation or depth
!c
!c written by:      Uli Mayer - January 24, 98
!c
!c last modified:   -
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   real*8:
!c           -------
!c           elevmax            = max. elevation in solution domain   + -
!c           z                  = z-coordinate in terms of elevation  + -
!c           zoutput            = z-coordinate for output             * +
!c
!c           logical:
!c           --------
!c           depth_output       = true.  -> output z-coordinate in    + -
!c                                          terms of depth below
!c                                          ground surface
!c
!c common:   -
!c
!c local:    -
!c
!c external: -  
!c ----------------------------------------------------------------------
 
      real*8 function zoutput(depth_output,z,elevmax)
 
      implicit none
      real*8 :: z, elevmax
      logical depth_output

      if (depth_output) then
        zoutput = elevmax - z
      else
        zoutput = z
      end if
 
      return
      end

