!c ----------------------------------------------------------------------
!c subroutine zero_i4_parallel
!c ------------------
!c
!c clear integer*4 array
!c
!c written by:      Uli Mayer - May 6, 96
!c
!c last modified:   Uli Mayer - November 27, 96
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   integer*4:
!c           ----------
!c           i4(n1*n2*n3)       = array to be cleared                 + -
!c           n1                 = first dimension of array            + -
!c           n2                 = second dimension of array           + -
!c           n3                 = third dimension of array            + -
!c
!c common:   -
!c
!c local:    integer*4:
!c           ----------
!c           i0                 = constant
!c           nentries           = number of entries        
!c           i                  = counter (number of entries
!c
!c external: -
!c ----------------------------------------------------------------------
 
      subroutine zero_i4_parallel(i4,n1,n2,n3)
      
#ifdef OPENMP
      use omp_lib 
      use gen, only : numofthreads_global, numofloops_thred_global
#endif 
 
      implicit none

      integer*4 i4(*), i0, i
      integer :: n1, n2, n3, nentries

      parameter (i0 = 0)
 
      nentries = n1*n2*n3

#ifdef OPENMP
    !$omp parallel                                                    &
    !$omp if (nentries > numofloops_thred_global)                     &
    !$omp num_threads(numofthreads_global)                            &
    !$omp default(shared)                                             &
    !$omp private (i)  
    !$omp do schedule(static)
#endif
      do i=1,nentries
        i4(i) = i0
      end do
#ifdef OPENMP
    !$omp end do
    !$omp end parallel
#endif
 
      return
      end
