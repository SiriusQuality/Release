!c ----------------------------------------------------------------------
!c real*8 function bulkconc
!c ------------------------
!c
!c convert from units [moles/l phase] to [moles/l bulk]
!c
!c written by:      Uli Mayer - August 15, 96
!c
!c last modified:   Uli Mayer - Jan 23, 98
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   concp            = concentration in phase p              + -
!c                              - [moles/l phase]
!c           satp             = phase saturation                      + -
!c           por              = porosity                              + -
!c
!c local:    -
!c
!c external: -
!c ----------------------------------------------------------------------
 
      real*8 function bulkconc(concp,satp,por)
 
      implicit none
      
      real*8 :: concp, satp, por

      bulkconc = satp*por*concp
  
      return
      end
