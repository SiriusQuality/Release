!c ----------------------------------------------------------------------
!c subroutine initweed
!c -------------------
!c
!c initialize work arrays for determination of secondary aqueous species, 
!c which are obsolete for simulation
!c
!c written by:      Uli Mayer - April 26, 98
!c
!c last modified:   -
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   -
!c
!c common:   
!c chem.f:   real*8:
!c           -------
!c           cxmax(nx)          = maximum concentration of secondary  * +
!c                                aqueous species in solution domain
!c           totcmin(n)         = minimum total aqueous component     * +
!c                                concentration in solution domain
!c
!c           integer*4:
!c           ----------
!c           nc                 = number of components excluding h2o  + -
!c           nx                 = number of secondary aqueous species + -
!c
!c           character:
!c           ----------
!c           namec(nc)          = component names                     + -
!c           namex(nx)          = names of secondary aqueous species  + - 
!c           
!c local:    real*8:
!c           -------
!c           r0                 = constant
!c           rlarge             = constant
!c
!c           integer*4:
!c           ----------
!c           ic                 = counter (components)
!c           ix                 = counter (secondary aqueous species)
!c           
!c
!c external: -
!c ----------------------------------------------------------------------
 
      subroutine initweed
 
      use parm
      use chem

      implicit none
      
      integer :: ic, ix

      real*8, parameter :: r0 = 0.0d0, rlarge = 1.d+300

!c  initialize work arrays

      do ic = 1,nc-1
        totcmin(ic) = rlarge
      end do

      do ix = 1,nx
        cxmax(ix) = r0
      end do

      return
      end

