!cprovi----------------------------------------------------------------------
!cprovi subroutine surf_tens_prop 
!cprovi Compute the water surface tension as a function of temperature 
!cprovi 
!cprovi Saito, H.; Simunek, J. & Mohanty, B. 
!cprovi Numerical analyses of coupled water, vapor and heat transport 
!cprovi in the vadose zone. Vadose Zone Journal, 2006, 5, 784-800
!c ----------------------------------------------------------------------
subroutine surf_tens_prop (surftens_ivol,dsurftensdt_ivol,temp_ivol)
                       
                       
use parm
use gen
use dens
use phys
use chem       
      
implicit none 
      
real*8, intent(in)             :: temp_ivol        ! temperature [oC]

real*8, intent(out)            :: surftens_ivol    ! surface tension  

real*8, intent(out)            :: dsurftensdt_ivol ! Derivative of the surface tension 
                 
real*8, parameter              :: r1=1.0d0, &
                                  r2=2.0d0, &
                                  r0=0.0d0, &
                                  rkelvin=273.15d0, &
                                  r1000=1.0d3, &
                                  r106=1.0d6, &
                                  r86400=86400.0d0, &
                                  dtempinc=1.0d-2, &
                                  cte1=75.6d0, &
                                  cte2=0.1425d0, &
                                  cte3=2.38d-4
                           
                           
 
dsurftensdt_ivol = r0
surftens_ivol = r0
!cprovi---------------------------------------------------------------------
!cprovi Compute surface tension 
!cprovi---------------------------------------------------------------------
surftens_ivol = (cte1 - cte2*temp_ivol - cte3*temp_ivol*temp_ivol) 
!cprovi---------------------------------------------------------------------
!cprovi Compute the derivative of surface tension 
!cprovi---------------------------------------------------------------------
dsurftensdt_ivol = (-cte2 - r2*cte3*temp_ivol) 




return
end
