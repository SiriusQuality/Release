!c ----------------------------------------------------------------------
!c subroutine rtrvpprm 
!c -------------------
!c
!c retrieve physical parameters for scaling
!c
!c written by:      Uli Mayer - November 18, 1996
!c
!c last modified:
!c
!c definition of variables:
!c
!c I --> on input   * arbitrary  - initialized  + entries expected
!c O --> on output  * arbitrary  - unaltered    + altered
!c                                                                    I O
!c passed:   real*8:
!c           -------
!c           swc                = water saturation for geochemical    * +
!c                                calculations
!c           sac                = air saturation for geochemical      * + 
!c                                calculations
!c           porc               = porosity used for geochemical       * + 
!c                                calculations
!c 
!c           character:
!c           ----------
!c           section_header     = text string identifying mode of     + -
!c                                assignment for physical parameters
!c
!c
!c common: 
!c phys.f:   real*8:
!c           -------
!c           por                = porosity                            + -
!c
!c local:    real*8:
!c           -------
!c           r0                 = constant
!c           r1                 = constant
!c
!c external: -
!c ----------------------------------------------------------------------
 
      subroutine rtrvpprm(swc,sac,porc,section_header)
 
      use parm
      use phys
 

      implicit none
      
      real*8 :: swc, sac, porc

      character*72 section_header 
      
      real*8 :: r0, r1

      parameter (r0 = 0.0d0, r1 = 1.0d0)

!c  initial condition - local geochemistry 
!c  fully saturated porous medium

      if (section_header.eq.      &
     &    'initial condition - local geochemistry') then

        swc = r1
        sac = r0
        porc = por

!c  initial condition - reactive transport
!c  fully saturated porous medium

      elseif (section_header.eq.  &
     &       'initial condition - reactive transport') then

        swc = r1
        sac = r0
        porc = por

!c  boundary conditions - reactive transport
!c  consider only water phase

      elseif (section_header.eq.  &
     &       'boundary conditions - reactive transport') then

        swc = r1
        sac = r0
        porc = r1

      end if
 
      return
      end
