    use solver_snes_common, only : strKSPType_flow, rtol_flow, abstol_flow, dtol_flow, maxits_flow,    &
                                   strKSPType_react, rtol_react, abstol_react, dtol_react,             &
                                   strKSPNormType_flow, strKSPNormType_react, maxits_react,            &
                                   strPCType_flow, strKSPConvergenceType_flow,                         &
                                   strPCType_react, strKSPConvergenceType_react,                       &
                                   pc_factor_shift_flow, pc_factor_shift_react,                        &
                                   b_use_petsc_default_flow, b_use_petsc_default_react,                &
                                   b_check_norm_flow, b_check_norm_react,                              &
                                   b_form_initial_guess_flow, b_form_initial_guess_react,              &
                                   b_initial_guess_nonzero_flow, b_reuse_preconditioner_flow,          &
                                   b_initial_guess_nonzero_react, b_reuse_preconditioner_react,        &
                                   stencil_width
#endif

      implicit none

      integer :: i
      character(len=256) :: arg, arg_pre

      !initialize some input arguments that can be read from both input file and solver configuration file
      b_solver_cfg_arg = .false.
      b_prtfile = .false.
      b_output_matrix = .false.
      b_output_matrix_petsc = .false.
      itimestep_output_matrix = -1
      b_output_condition_number = .false.

      prefix = 'min3p'
      write(*,*) 'Voila la valeur de prefix : ', prefix
      return
     ! if(command_argument_count() == 0) then
     !   read(*,'(a256)') prefix
     !   return
     ! end if

      !i = 0 -> program name
      !i = 1 -> input file name
      call get_command_argument(0, arg)
      if(rank == 0) then
        write(*, '(2a)') "Program path: ", trim(arg)
      end if

      call get_command_argument(1, arg)
      !Check if the first parameter is command, which begins with "-"
      if(arg(1:1) /= "-") then  
        if(rank == 0) then
          write(*, '(2a)') "Input file name: ", trim(arg)
        end if
        prefix = arg        
      end if

      if(command_argument_count() < 2) then
        return
      else
        call initsolver_default
      end if

      arg_pre = ""
      if(arg(1:1) == "-") then
        i = 1
      else
        i = 2
      end if
      
#ifdef PETSC
      b_use_petsc_default_flow = .false.
      b_use_petsc_default_react = .false.
      b_check_norm_flow = .false.
      b_check_norm_react = .false.
#endif

      do
        call get_command_argument(i, arg)
        call lowercase(arg)

        select case (trim(arg_pre))

          case ("-solver_configuration_file")
              str_solvercfg = trim(arg)
              if (rank == 0) then
                write (*,'(2a)') "solver configuration file: ", trim(arg)
              end if
              b_solver_cfg_arg = .true.
              return
              
          case ("-input_file")
              prefix = trim(arg)
              if(rank == 0) then
                write(*, '(2a)') "Input file name: ", trim(arg)
              end if

          case ("-solver_type_flow")
              read(arg, *) i_solver_type_flow
#ifdef PETSC
              if (nprcs > 1) then
                i_solver_type_flow = 2
                if(rank == 0) then
                  write(*,'(a,i6)') "Flow solver type is forced to: ", i_solver_type_flow
                end if
              else
                if(rank == 0) then
                  write(*,'(a,i6)') "Flow solver type is: ", i_solver_type_flow
                end if
              end if
#else
              if(rank == 0) then
                write(*,'(a,i6)') "Flow solver type is: ", i_solver_type_flow
              end if
#endif

          case ("-solver_type_react")
              read(arg, *) i_solver_type_react
#ifdef PETSC
              if (nprcs > 1) then
                i_solver_type_react = 2
                if(rank == 0) then
                  write(*,'(a,i6)') "Reactive transport solver type is forced to: ", i_solver_type_react
                end if
              else
                if(rank == 0) then
                  write(*,'(a,i6)') "Reactive transport solver type is: ", i_solver_type_react
                end if
              end if
#else
              if(rank == 0) then
                write(*,'(a,i6)') "Reactive transport solver type is: ", i_solver_type_react
              end if
#endif


          case ("-use_numofthreads_from_mpi")
#ifdef MPI
