subroutine initsolver_final

#ifdef PARDISO
    use solver_pardiso, only :  numofthreads_pardiso, pardiso_initialize_common, pardiso_initialize_flow, pardiso_initialize_react
#endif

    implicit none
    
    logical :: bstat
    
    bstat = .false.

    !Set nthreads
#ifdef OPENMP
    nthreads = numofthreads_global
    if (i_solver_type_flow == 0 .or. i_solver_type_react == 0) then
        if(nthreads < numofthreads_ws209) then
            nthreads = numofthreads_ws209
        end if
    end if
#ifdef PARDISO
    if (i_solver_type_flow == 1 .or. i_solver_type_react == 1) then
        if(nthreads < numofthreads_pardiso) then
            nthreads = numofthreads_pardiso
        end if
    end if
#endif

    if(i_matrix_assembly_type_flow > 0) then
        if(nthreads < numofthreads_matrix_flow) then
            nthreads = numofthreads_matrix_flow
        end if
    end if
    
    if(i_matrix_assembly_type_react > 0) then
        if(nthreads < numofthreads_matrix_react) then
            nthreads = numofthreads_matrix_react
        end if
    end if
    
    call OMP_SET_NUM_THREADS(numofthreads_global)
#endif
   
    if (i_solver_type_flow == 1 .or. (i_solver_type_flow == 0 .and. b_solver_test_pardiso .eqv. .true.)) then
#ifdef PARDISO   
        call pardiso_initialize_flow
        bstat = .true.
#else
        i_solver_type_flow = 0
        if(rank == 0) then
          write(*,'(a)') "The program is compiled without the environment of Pardiso, use the default solver ws209 instead."
        end if
#endif
    end if
    
    if (i_solver_type_react == 1 .or. (i_solver_type_react == 0 .and. b_solver_test_pardiso .eqv. .true.)) then
#ifdef PARDISO   
        call pardiso_initialize_react
        bstat = .true.
#else
        i_solver_type_react = 0
        if(rank == 0) then
          write(*,'(a)') "The program is compiled without the environment of Pardiso, use the default solver ws209 instead."
        end if
#endif
    end if
    
    if (bstat) then
#ifdef PARDISO
        call pardiso_initialize_common
#else
        if(rank == 0) then
          write(*,'(a)') "The program is compiled without the environment of Pardiso, use the default solver ws209 instead."
        end if
#endif
    end if

end subroutine initsolver_final


subroutine solver_get_args()

      use file_utility, only : lowercase

#ifdef PARDISO
